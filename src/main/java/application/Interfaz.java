    private void update_ejecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_ejecutarActionPerformed
        // TODO add your handling code here:
        
        FacturaJpaController facturaJPA = new FacturaJpaController();
        
        Factura actualizarFact = new Factura();
        
        try {
            
            actualizarFact.setCodigo(Integer.parseInt(create_cr.getText()));
                
            // Array para recoger la fecha
            String[] tokens;

            tokens = create_fr.getText().split("-"); // Dividimos los datos y lo almacenamos en el array


            int anio = Integer.parseInt(tokens[0]);
            int month = Integer.parseInt(tokens[1]);
            int day = Integer.parseInt(tokens[2]);

            // Creamos una nueva instancia de tipo DATE para almacenar los datos de la fecha
            Date fecha = new Date(anio, month, day);

            actualizarFact.setFechaEmision(fecha);

            actualizarFact.setDescripcion(create_dr.getText());

            // Creamo una instancia para almacenar el dato después de parsearlo
            BigDecimal total =  new BigDecimal(Double.parseDouble(create_tr.getText()));

            actualizarFact.setTotalImporteFactura(total);
            
            // Realizar update usando el método edit
            facturaJPA.edit(actualizarFact);
            create_right.setVisible(true); // Se ejecuta si la actualización sale correcta
            
        } catch (Exception ex) {
            Logger.getLogger(Interfaz.class.getName()).log(Level.SEVERE, null, ex);
            create_wrong.setVisible(true);
        }
        
    }//GEN-LAST:event_update_ejecutarActionPerformed

