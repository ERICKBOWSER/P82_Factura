    private void create_ejecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_create_ejecutarActionPerformed
        // TODO add your handling code here:
        Factura nuevaFactura = new Factura();
        
        nuevaFactura.setCodigo(Integer.parseInt(create_cr.getText()));
                
        // Array para recoger la fecha
        String[] tokens;
        
        tokens = create_fr.getText().split("-"); // Dividimos los datos y lo almacenamos en el array

        
        int anio = Integer.parseInt(tokens[0]);
        int month = Integer.parseInt(tokens[1]);
        int day = Integer.parseInt(tokens[2]);
        
        // Creamos una nueva instancia de tipo DATE para almacenar los datos de la fecha
        Date fecha = new Date(anio, month, day);
        
        nuevaFactura.setFechaEmision(fecha);
        
        nuevaFactura.setDescripcion(create_dr.getText());
        
        // Creamo una instancia para almacenar el dato después de parsearlo
        BigDecimal total =  new BigDecimal(Double.parseDouble(create_tr.getText()));
        
        nuevaFactura.setTotalImporteFactura(total);
        
        // Creamos una nueva instancia de la Factura JPA
        FacturaJpaController facturaJPA = new FacturaJpaController();
        
        try {
            facturaJPA.create(nuevaFactura);
            create_right.setVisible(true);
        } catch (Exception ex) {
            Logger.getLogger(Interfaz.class.getName()).log(Level.SEVERE, null, ex);
            create_wrong.setVisible(true);
        }
        
    }//GEN-LAST:event_create_ejecutarActionPerformed

    private void comprobarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comprobarActionPerformed
        // TODO add your handling code here:
        // Creamos una nueva instancia de la Factura JPA
        FacturaJpaController facturaJPA = new FacturaJpaController();
        
        Factura encontrarFact = new Factura();
        
        encontrarFact = facturaJPA.findFactura(Integer.parseInt(create_cr.getText()));
        
        if(encontrarFact != null){
            comprobar.setVisible(false);
            
            create_f.setVisible(true);
            create_d.setVisible(true);
            create_t.setVisible(true);

            create_fr.setVisible(true);
            create_dr.setVisible(true);
            create_tr.setVisible(true);
            
            create_fr.setText(encontrarFact.getFechaEmision().toString());
            create_dr.setText(encontrarFact.getDescripcion().toString());
            create_tr.setText(encontrarFact.getTotalImporteFactura().toString());
            
        }else{
            comprobar_error.setVisible(true);
        }
        
    }//GEN-LAST:event_comprobarActionPerformed

